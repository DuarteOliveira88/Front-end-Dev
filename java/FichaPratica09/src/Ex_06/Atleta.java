package Ex_06;

public class Atleta {
    private String nome;
    private String modalidadeDesportiva;
    private double altura;
    private double peso;
    private String pais;

    public Atleta(String nome, String modalidadeDesportiva, double altura, double peso, String pais) {
        this.nome = nome;
        this.modalidadeDesportiva = modalidadeDesportiva;
        this.altura = altura;
        this.peso = peso;
        this.pais = pais;
    }

}
