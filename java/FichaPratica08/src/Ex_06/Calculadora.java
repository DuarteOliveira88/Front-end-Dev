package Ex_06;

public class Calculadora {

    public void adicao(double num1, double num2){
        System.out.println("A adiçao dos numeros "+ num1+" "+num2+" e: "+(num1+num2));
    }
    public void sub(double num1, double num2){
        System.out.println("A subtraçao dos numeros "+ num1+" "+num2+" e: "+(num1-num2));
    }
    public void div(double num1, double num2){
        System.out.println("A divisao dos numeros "+ num1+" "+num2+" e: "+(num1/num2));
    }
    public void mult(double num1, double num2){
        System.out.println("A multiplicaçao dos numeros "+ num1+" "+num2+" e: "+(num1*num2));
    }
}
